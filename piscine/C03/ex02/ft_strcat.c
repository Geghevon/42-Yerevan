char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	ds;

	i = 0;
	ds = 0;
	while (dest[ds] != '\0')
		ds++;
	while (src[i] != '\0')
	{
		dest[ds] = src[i];
		ds++;
		i++;
	}
	dest[ds] = '\0';
	return (dest);
}
