#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	num;

	i = 0;
	n = 1;
	num = 0;
	while ((str[i] == ' ') || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if ((str[i] == '-' || str[i] == '+')
		&& (str[i + 1] >= 48 && str[i + 1] <= 57))
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (str[i] - '0') + (num * 10);
		i++;
	}
	return (num * n);
}
