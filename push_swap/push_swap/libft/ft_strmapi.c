#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t			len;
	char			*string;
	unsigned int	i;

	i = 0;
	len = ft_strlen(s);
	string = malloc(sizeof(char) * (len + 1));
	if (!s || !f)
		return (0);
	while (s[i])
	{
		string[i] = f(i, s[i]);
		i++;
	}
	string[i] = '\0';
	return (string);
}
